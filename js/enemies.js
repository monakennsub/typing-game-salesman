/**
 * 敵情報を取得
 */
function getEnemy(stageId) {
    return ENEMIES[stageId] || null;
}

/**
 * ランダムなセリフを取得
 */
function getRandomDialogue(stageId) {
    const enemy = getEnemy(stageId);
    if (!enemy || !enemy.dialogue) return "来い！";
    return enemy.dialogue[Math.floor(Math.random() * enemy.dialogue.length)];
}

/**
 * ランダムなタイピングプロンプトを取得
 */
function getRandomTypingPrompt(stageId) {
    const enemy = getEnemy(stageId);
    if (!enemy || !enemy.typingPrompts) return "テキストを入力してください";
    return enemy.typingPrompts[Math.floor(Math.random() * enemy.typingPrompts.length)];
}

/**
 * 敵のHPをステージに基づいて計算
 */
function calculateEnemyHP(stageId) {
    const enemy = getEnemy(stageId);
    if (!enemy) return 100;
    return enemy.hp;
}
